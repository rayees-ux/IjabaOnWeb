const allAdhkar = [...morningAdhkar, ...eveningAdhkar, ...sleepAdhkar];

let currentCategory = 'all';
let currentSearchQuery = '';

function createDhikrCard(dhikr) {
    return `
        <div class="dhikr-card fade-in">
            <div class="dhikr-header">${dhikr.malayalam}</div>
            <div class="dhikr-arabic">${dhikr.arabic}</div>
            <div class="dhikr-english">${dhikr.english}</div>
            <div class="dhikr-buttons">
                <button class="btn-dhikr btn-copy" type="button" onclick="copyToClipboard('${dhikr.arabic.replace(/'/g, "\\'")}')">📋 പകർത്തുക</button>
                <button class="btn-dhikr btn-save" type="button" onclick="saveDhikr('${dhikr.malayalam.replace(/'/g, "\\'")}', '${dhikr.arabic.replace(/'/g, "\\'")}')">⭐ സേവ് ചെയ്യുക</button>
                <button class="btn-dhikr btn-play" type="button" onclick="playDhikr('${dhikr.arabic.replace(/'/g, "\\'")}')">🔊 പ്ലേ</button>
            </div>
        </div>
    `;
}

function renderDhikrCards(dhikrs) {
    const grid = document.getElementById('dikr-container');
    const noResults = document.getElementById('noResults');

    if (!grid) {
        console.warn('Dhikr container not found');
        return;
    }

    if (dhikrs.length === 0) {
        grid.innerHTML = '';
        noResults.style.display = 'block';
    } else {
        grid.innerHTML = dhikrs.map(createDhikrCard).join('');
        noResults.style.display = 'none';
    }
}

function filterDhikrs(query = currentSearchQuery, category = currentCategory) {
    let filtered = allAdhkar;

    if (category !== 'all') {
        filtered = filtered.filter(dhikr => dhikr.category === category);
    }

    if (query.trim()) {
        const lowerQuery = query.toLowerCase();
        filtered = filtered.filter(dhikr =>
            dhikr.malayalam.toLowerCase().includes(lowerQuery) ||
            dhikr.english.toLowerCase().includes(lowerQuery) ||
            dhikr.arabic.toLowerCase().includes(lowerQuery)
        );
    }

    return filtered;
}

function showAdhkar(category) {
    currentCategory = category;
    document.querySelectorAll('.tab-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.category === category);
    });
    updateDisplay();
}

function updateDisplay() {
    const filtered = filterDhikrs();
    renderDhikrCards(filtered);
}

function copyToClipboard(text) {
    if (!navigator.clipboard) {
        const textarea = document.createElement('textarea');
        textarea.value = text;
        document.body.appendChild(textarea);
        textarea.focus();
        textarea.select();
        document.execCommand('copy');
        document.body.removeChild(textarea);
        alert('ദിഖ്റ് ക്ലിപ്പ്ബോർഡിലേക്ക് പകർത്തി!');
        return;
    }

    navigator.clipboard.writeText(text).then(() => {
        alert('ദിഖ്റ് ക്ലിപ്പ്ബോർഡിലേക്ക് പകർത്തി!');
    }).catch(err => {
        console.error('Failed to copy: ', err);
        alert('ക്ലിപ്പ്ബോർഡിലേക്ക് പകർത്താൻ പറ്റിയില്ല. ദയവായി വീണ്ടും ശ്രമിക്കുക.');
    });
}

function saveDhikr(title, text) {
    const saved = JSON.parse(localStorage.getItem('savedDhikrs') || '[]');
    const dhikr = { title, text, savedAt: new Date().toISOString() };

    if (saved.some(d => d.title === title)) {
        alert('ഈ ദിഖ്റ് മുമ്പേ സേവ് ചെയ്തു കഴിഞ്ഞു!');
        return;
    }

    saved.push(dhikr);
    localStorage.setItem('savedDhikrs', JSON.stringify(saved));
    alert('ദിഖ്റ് ഫേവറിറ്റ്സിൽ സേവ് ചെയ്തിരിക്കുന്നു!');
}

function playDhikr(text) {
    alert('ഓഡിയോ ഫീച്ചർ വരാനിരിക്കുന്നു: ' + text.substring(0, 50) + '...');
}

document.addEventListener('DOMContentLoaded', function() {
    const searchInput = document.getElementById('dhikrSearch');
    const tabButtons = document.querySelectorAll('.tab-btn');

    tabButtons.forEach(button => {
        button.addEventListener('click', function() {
            showAdhkar(this.dataset.category);
        });
    });

    searchInput.addEventListener('input', function() {
        currentSearchQuery = this.value;
        updateDisplay();
    });

    showAdhkar(currentCategory);
});
